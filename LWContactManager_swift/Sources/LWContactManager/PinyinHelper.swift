    /// Get the first letter of pinyin for a Chinese character
    /// - Parameter character: The Chinese character
    /// - Returns: The first letter in uppercase, or '#' if not a Chinese character
    public static func pinyinFirstLetter(for character: Character) -> Character {
        let string = String(character)

        // Try to get pinyin using CFStringTransform
        if let mutableString = NSMutableString(string: string) as CFMutableString? {
            if CFStringTransform(mutableString, nil, kCFStringTransformToLatin, false),
               CFStringTransform(mutableString, nil, kCFStringTransformStripDiacritics, false) {
                let pinyin = mutableString as String
                if let firstChar = pinyin.uppercased().first, firstChar.isLetter {
                    return firstChar
                }
            }
        }

        // Fallback to C implementation if needed
        if let scalar = character.unicodeScalars.first {
            return pinyinFirstLetterFromC(UInt16(scalar.value))
        }

        return "#"
    }

    /// Get the first letter of pinyin for a string (uses first character)
    /// - Parameter string: The input string
    /// - Returns: The first letter in uppercase, or '#' if empty or not Chinese
    public static func pinyinFirstLetter(for string: String) -> Character {
        guard let firstChar = string.first else { return "#" }
        return pinyinFirstLetter(for: firstChar)
    }

